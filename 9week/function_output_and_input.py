# print() 함수는 기본적으로 반환값이 None입니다.
# 아래의 코드는 print() 함수의 반환값을 변수 v에 저장한 후 출력하는 예제입니다.
v = print('안녕~~')  # '안녕~~'이 출력되고, v에는 None이 저장됩니다.
print(v)  # v의 값인 None이 출력됩니다.

# input() 함수는 사용자로부터 입력을 받고 그 입력값을 반환합니다.
# 아래의 코드는 사용자로부터 문자열을 입력받아 출력하는 예제입니다.
r = input('사용자 입력 : ')  # 사용자 입력을 받는 프롬프트를 표시합니다.
print(r)  # 사용자가 입력한 값이 출력됩니다.
