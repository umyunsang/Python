# for문과 while문을 사용하여 동일한 결과를 출력하는 프로그램

# for문을 사용한 반복
# range(3)은 0부터 2까지의 숫자(총 3개)를 생성합니다.
for i in range(3):
    print('안녕')  # 반복문이 3회 실행되어 '안녕'이 3번 출력됩니다.

# while문을 사용한 반복
# 변수 i를 0으로 초기화합니다.
i = 0
# 조건문: i가 3보다 작을 때까지 반복합니다.
while i < 3:
    print('안녕')  # 조건이 참일 때 반복하여 '안녕'을 출력합니다.
    i += 1  # i를 1씩 증가시킵니다. 조건이 거짓이 될 때까지 반복합니다.
