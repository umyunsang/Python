# 리스트 생성
my_list = [22, 33, 44]

# 양수 인덱스를 사용하여 리스트의 요소를 출력
print(my_list[0])  # 첫 번째 요소 출력: 22
print(my_list[1])  # 두 번째 요소 출력: 33
print(my_list[2])  # 세 번째 요소 출력: 44

# 음수 인덱스를 사용하여 리스트의 요소를 출력 (뒤에서부터 세기)
print(my_list[-1])  # 마지막 요소 출력: 44
print(my_list[-2])  # 뒤에서 두 번째 요소 출력: 33
print(my_list[-3])  # 뒤에서 세 번째 요소 출력: 22

# 문자열에서 음수 인덱스를 사용하여 마지막 문자 출력
msg = '안녕하세요'
print(msg[-1])  # 문자열의 마지막 문자 출력: '요'
