# 리스트를 생성하고 초기화
my_list = [1]
print(my_list)  # 초기 리스트를 출력: [1]

# append() 메소드를 사용하여 리스트에 요소를 추가
my_list.append(2)  # 리스트에 2를 추가
my_list.append(3)  # 리스트에 3을 추가
my_list.append(4)  # 리스트에 4를 추가
my_list.append(5)  # 리스트에 5를 추가
my_list.append(6)  # 리스트에 6을 추가

# 요소가 추가된 리스트를 출력
print(my_list)  # 최종 리스트: [1, 2, 3, 4, 5, 6]
