# 튜플(tuple) : 값을 여러 개 보유하는 정보체
# 튜플은 소괄호로 정의되며, 여러 개의 값을 하나의 변수에 저장할 수 있습니다.
# (값1, 값2, 값3) 형식으로 정의하며, 소괄호는 생략할 수도 있습니다.

# 튜플 생성
a = (100, 200, 300)

# 튜플 언패킹: 튜플의 각 값을 개별 변수에 할당
n1, n2, n3 = a  # a 튜플의 첫 번째 값은 n1, 두 번째 값은 n2, 세 번째 값은 n3에 할당됨

# 튜플을 출력
print(a)       # 출력: (100, 200, 300)

# 튜플의 데이터 타입을 출력
print(type(a)) # 출력: <class 'tuple'>

# 언패킹된 변수들을 출력
print(n1, n2, n3)  # 출력: 100 200 300
