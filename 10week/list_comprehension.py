# List Comprehension (리스트 컴프리헨션)
# 리스트를 간결하게 생성할 수 있는 방법으로, 반복문과 조건문을 이용하여 리스트를 만듭니다.

# 예: 3의 배수 8개를 갖는 리스트를 생성하는 두 가지 방법

# 방법 1: 빈 리스트를 먼저 만들고, 반복문을 통해 값을 추가하는 방식
my_list = []  # 빈 리스트 생성
for x in range(1, 9):  # 1부터 8까지 반복
    my_list.append(x * 3)  # 3의 배수를 리스트에 추가
print(my_list)  # 결과 출력: [3, 6, 9, 12, 15, 18, 21, 24]

# 방법 2: 리스트 컴프리헨션을 사용하여 간결하게 리스트를 생성하는 방식
my_list = [x * 3 for x in range(1, 9)]  # 1부터 8까지의 숫자 각각에 3을 곱하여 리스트 생성
print(my_list)  # 결과 출력: [3, 6, 9, 12, 15, 18, 21, 24]
