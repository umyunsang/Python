# 1.사용자의 대출 가능한 책의 수는 최대 3권입니다.
# 2. 책을 대출하기 위해서 사용자의 등급이 골드 아님 실버
# 3. 브론즈는 절대 대출 금지
# 4.특별 이벤트 기간에 모든 회원 책 추가로 1권 더 대출

# 도서 대출 가능 여부를 판단하는 프로그램

# 현재 대출하고 있는 책의 수를 사용자로부터 입력받음
book = int(input('현재 대출하고 있는 책의 수 : '))

# 사용자의 회원 등급을 입력받음
grade = input("회원 등급 : ")

# 특별 이벤트 기간 여부를 입력받음
event = input("특별 이벤트 기간인가요? (예/아니요) : ")

# 회원 등급에 따라 대출 가능 여부를 결정
if grade == '브론즈':
    print("브론즈 등급은 대출이 불가합니다.")
else:
    # 기본적으로 대출 가능한 책의 수는 3권
    r_book = 3

    # 특별 이벤트 기간인 경우 대출 가능한 책의 수를 1권 추가
    if event == '예':
        r_book += 1

    # 대출 가능한 책의 수에서 현재 대출 중인 책의 수를 차감
    r_book -= book

    # 대출 가능한 책의 수에 따라 메시지 출력
    if r_book <= 0:
        print("더 이상 대출할 수 없습니다.")
    else:
        print(f'추가로 {r_book}권 대출 가능합니다.')

# 추가: 잘못된 입력 처리 (회원 등급이나 이벤트 입력값이 예상치 못한 값일 때)
if grade not in ['골드', '실버', '브론즈']:
    print("잘못된 회원 등급입니다. '골드', '실버', '브론즈' 중에서 입력해 주세요.")
if event not in ['예', '아니요']:
    print("잘못된 입력입니다. '예' 또는 '아니요'로 입력해 주세요.")
