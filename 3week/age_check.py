# 나이에 따라 다른 메시지를 출력하는 프로그램

# 사용자로부터 나이를 입력받음
age = int(input("나이를 입력 ==> "))

# 나이에 따라 조건을 평가하여 적절한 메시지를 출력
if age < 18:
    # 나이가 18세 미만일 때 출력
    print("집에 갈 시간이네요!")
else:
    # 나이가 18세 이상일 때 출력
    print("협조 감사합니다.")

