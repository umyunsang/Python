# 복합 대입 연산자를 사용하여 여행 경비를 계산하는 프로그램

# 초기 총 경비를 0으로 설정
total = 0

# 여행자 총 인원 수를 입력받음 (정수형으로 변환)
a = int(input("여행자 총 인원 수 : "))

# 숙박비용을 입력받아 총 경비에 추가 (정수형으로 변환)
b = int(input("숙박비용 : "))
total += b  # 복합 대입 연산자 사용

# 식비를 입력받아 총 경비에 추가 (정수형으로 변환)
c = int(input("식비 : "))
total += c  # 복합 대입 연산자 사용

# 기타 경비를 입력받아 총 경비에 추가 (정수형으로 변환)
d = int(input("기타 경비 : "))
total += d  # 복합 대입 연산자 사용

# 전체 여행 경비와 1인당 금액을 출력 (f-string 사용)
print(f'전체 여행 경비는 {total}원이며, 인원 수가 {a}명 일 때, 각자 금액 : {total / a}원')
